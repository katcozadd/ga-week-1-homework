for (let i = 0; i <= 99; i++) {
	console.log(i);
}

//Loop 2
for (let i = 99; i >= 0; i--) {
	console.log(i);
}

//Loop 3
for (let i = 0; i <= 98; i+=2) {
	console.log(i);
}

//Loop 4
for (let i = 98; i >= 0; i-=2) {
	console.log(i);
}

